package com.vde.object;

public class Artist {

	private int artistId;
	private String name;
	
	
	public int getArtistId() {
		return artistId;
	}
	public void setArtistId(int authorId) {
		this.artistId = authorId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
